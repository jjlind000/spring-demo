package com.java2novice_3.bean;
 
public class Order {
 
    private String item;
     
    private String price;
     
    public String getItem() {
        return item;
    }
    public void setItem(String item) {
        this.item = item;
    }
    public String getPrice() {
        return price;
    }
    public void setPrice(String price) {
        this.price = price;
    }
}